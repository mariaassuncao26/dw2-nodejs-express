//Variaveis podem ser declaradas de 3 formas:
//Var, Let e Const

//VAR: no geral evite o seu uso, pode não ser muito seguro.
//LET: Utilize quando não for necessario reatribuir o valor da variavel.
//CONST: Utilize quando NÃO precisar reatribuir o valor da variavel.

var cidade = "Registro";
var cidade = "Sete Barras";
document.write(cidade);

let nome = "Diego";
nome = "Maria";
document.write("<br>")
document.write(nome);

const pais = "Brasil"
pais = "Argentina"
//Valor de uma constante não pode ser alterado
document.write("<br>")
document.write(pais);